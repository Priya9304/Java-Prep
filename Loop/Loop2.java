class Loop2{
	public static void main(String[] args){
		while(true){
			System.out.println("Hello");
			break;
		}
		int i=1;
		while(i<=3){
			System.out.print(i+" ");
			i++;
		}
		System.out.println("\n---------------------------------------------------");
		int j=5;
		while(j>=1){
			System.out.print(j+" ");
			j-=1;
		}
	}
}
